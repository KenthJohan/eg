typedef enum
{
	EG_WINDOW_FULLSCREEN = 0x00000001,         /**< fullscreen window */
	EG_WINDOW_OPENGL = 0x00000002,             /**< window usable with OpenGL context */
	EG_WINDOW_SHOWN = 0x00000004,              /**< window is visible */
	EG_WINDOW_HIDDEN = 0x00000008,             /**< window is not visible */
	EG_WINDOW_BORDERLESS = 0x00000010,         /**< no window decoration */
	EG_WINDOW_RESIZABLE = 0x00000020,          /**< window can be resized */
	EG_WINDOW_MINIMIZED = 0x00000040,          /**< window is minimized */
	EG_WINDOW_MAXIMIZED = 0x00000080,          /**< window is maximized */
	EG_WINDOW_MOUSE_GRABBED = 0x00000100,      /**< window has grabbed mouse input */
	EG_WINDOW_INPUT_FOCUS = 0x00000200,        /**< window has input focus */
	EG_WINDOW_MOUSE_FOCUS = 0x00000400,        /**< window has mouse focus */
	EG_WINDOW_FULLSCREEN_DESKTOP = ( EG_WINDOW_FULLSCREEN | 0x00001000 ),
	EG_WINDOW_FOREIGN = 0x00000800,            /**< window not created by SDL */
	EG_WINDOW_ALLOW_HIGHDPI = 0x00002000,      /**< window should be created in high-DPI mode if supported.
													 On macOS NSHighResolutionCapable must be set true in the
													 application's Info.plist for this to have any effect. */
	EG_WINDOW_MOUSE_CAPTURE    = 0x00004000,   /**< window has mouse captured (unrelated to MOUSE_GRABBED) */
	EG_WINDOW_ALWAYS_ON_TOP    = 0x00008000,   /**< window should always be above others */
	EG_WINDOW_SKIP_TASKBAR     = 0x00010000,   /**< window should not be added to the taskbar */
	EG_WINDOW_UTILITY          = 0x00020000,   /**< window should be treated as a utility window */
	EG_WINDOW_TOOLTIP          = 0x00040000,   /**< window should be treated as a tooltip */
	EG_WINDOW_POPUP_MENU       = 0x00080000,   /**< window should be treated as a popup menu */
	EG_WINDOW_KEYBOARD_GRABBED = 0x00100000,   /**< window has grabbed keyboard input */
	EG_WINDOW_VULKAN           = 0x10000000,   /**< window usable for Vulkan surface */
	EG_WINDOW_METAL            = 0x20000000,   /**< window usable for Metal view */

	EG_WINDOW_INPUT_GRABBED = EG_WINDOW_MOUSE_GRABBED
} eg_window_flag_t;


